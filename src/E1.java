public class E1 {
    /*
    * Crie um programa que utilize um loop while para imprimir, em linhas separadas, os números de 1 até 50.
        Requisitos:

        - Utilizar a estrutura de repetição while;
        - Imprimir cada número em uma nova linha. Crie um programa que utilize um loop while para imprimir, em linhas separadas, os números de 1 até 50.
        Requisitos:
        - Utilizar a estrutura de repetição while;
        - Imprimir cada número em uma nova linha.
    * */

    public static void main(String[] args) {
        int i = 1;
        while (i <= 50) {
            System.out.println(i);
            i++;
        }
    }
}
